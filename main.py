# запрашиваем строку у пользователя
user_input = input("Введите строку: ")
# определяем согласные буквы русского алфавита
consonants = "йцкнгшщзхъфвпрлджчсмтьбЙЦКНГШЩЗХЪФВПРЛДЖЧСМТЬБ"
# инициализируем список для хранения индексов согласных
consonant_indices = []
# проходим по каждому символу в строке с его индексом
for index, char in enumerate(user_input):
        # проверяем, является ли символ согласной буквой
        if char in consonants:
                # если да, то добавляем индекс в список
                consonant_indices.append(index)
# выводим индексы согласных букв
print(f"Индексы согласных букв: {consonant_indices}")
